<h1>PÁGINA DE NOVEDADES</h1>
<br><br>

Camilo Echeverry Correa (Medellín, 16 de marzo de 1994), también conocido como Camilo, es un músico, 
cantante, compositor y multinstrumentista colombiano. 
<br><br>
Saltó a la fama en 2007 después de ganar el concurso de talentos Factor X de su país.
Es conocido por sus éxitos «Tutu», junto a Pedro Capó,«Desconocidos» y «La boca», con Mau y Ricky, 
también es famoso por escribir éxitos para otros cantantes como Becky G y Natti Natasha en «Sin Pijama»
o Lali y Mau & Ricky en «Sin querer queriendo».